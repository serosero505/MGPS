                <h2>Caracteristicas</h2>
                <h3>SCRUM</h3>
                <br>
                <p>Es una metodología agíl, iterativa, dispuesta al cambio, basando su desarrollo en principios de inspección y adaptación que se aplica en los procesos de gestion de proyectos de software, es un tipo de desarrollo que permite minimizar los riesgos en la realización de un proyecto.</p>
                <p>Las actividades de la metodología se repiten con cada iteracción y el producto a su vez se va incrementando, esto se logra a medida que se van adaptando y completando nuevas funcionalidades que se logran al final de un sprint.</p>
                <p>Scrum como metodología y caracteristica mas importante, incluye al cliente como parte del equipo de trabajo y esta comprometido con el objetivo del proceso.
                    <br>Esta metodologia tiene 3 pilares para el control de procesos:
                    <br><strong>1. Transparencia:</strong> 
                    <br>Todo el miembro del equipo debe compartir cada dificultad que tenga, ademas de ser honesto y transparente al realizar su trabajo para que el proceso tenga el mejor desarrollo posible.
                    <br>
                    <br><strong>2. Inspeccion:</strong>El producto es revisado constantemente en busca de mejoras.
                    <br>
                    <br><strong>3. Adaptación</strong>Al final de cada sprint o ciclo de trabajo, el equipo se pone de acuerdo para realizar cambios para que el siguiente sprint sea mejor que el anterior es decir adaptando la dinámica de trabajo al entorno del cliente y del desarrollo.</p>

